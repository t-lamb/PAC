import javax.swing.JOptionPane; //import from API, becomes part of the symbol table

public class Math1 {
	
	public static void main(String args[]) {
		System.out.println ("5%2 =  " + 5 % 2);
		System.out.println ("17%5 =  " + 17 % 5);
		System.out.println ("32%8 =  " + 32 % 8);

		System.out.println ("24/5 =  " + 24 / 5);
		System.out.println ("32/8 =  " + 32 / 8);
		System.out.println ("3/4 =  " + 3 / 4);

		System.out.println ("1234/1000 =  " + 1234 / 1000);
		System.out.println ("1234%1000 =  " + 1234 % 1000);
		System.out.println ("234/100 =  " + 234 / 100);
		System.out.println ("234%100 =  " + 234 % 100);
		System.out.println ("34/10 =  " + 34 / 10);
		System.out.println ("34%10 =  " + 34 % 10);

	}//end method main
}